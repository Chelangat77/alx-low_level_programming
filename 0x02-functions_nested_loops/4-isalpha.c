/**
 * _isalpha - Entry point
 *
 * Description: checks is the output is a letter
 *
 * @c: the integer value it receives
 *
 * Return: 1 if true. 0 if false.
 */

int _isalpha(int c)
{
	int i = 'a';
	int y = 'A';

	for (i = 'a'; i <= 'z'; i++)
	{
	if (c == 1)

	return (1);
	}

	for (y = 'A'; y <= 'Z'; y++)
	{
	if (c == y)

	return (1);
	}

		return (0);
}
